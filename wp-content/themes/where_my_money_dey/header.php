            var request = $.ajax({
                url: "../actnow/index.php/subscribe/email_petitions",
	data: "email="+document.getElementById("email_p").value,
                type: "GET",            
                dataType: "html"
            });
		
            request.done(function(msg) {
	$('#email_p').val("");
                $("#mybox").html(msg);
	$('#email_p').hide();
	$('#submit_p').hide();          
            });

            request.fail(function(jqXHR, textStatus) {
                alert( "Request failed: " + textStatus );
            });
        }
